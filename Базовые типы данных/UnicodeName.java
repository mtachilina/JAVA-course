/**
 * Программа для вывода имени "Мария" с использованием Unicode.
 * 
 * <p>Демонстрирует использование escape-последовательностей \uXXXX
 * для представления символов Unicode в Java.</p>
 * 
 * @author Тачилина Мария
 * @version 1.0
 * @since 2026-04-24
 */
public class UnicodeName {
    
    /**
     * Главный метод программы.
     * 
     * <p>Выводит в консоль имя "Мария", используя Unicode 
     * escape-последовательности для каждого символа.</p>
     * 
     * <p>Таблица соответствия символов и Unicode-кодов:</p>
     * <ul>
     *   <li>М — \u041C</li>
     *   <li>а — \u0430</li>
     *   <li>р — \u0440</li>
     *   <li>и — \u0438</li>
     *   <li>я — \u044F</li>
     * </ul>
     * 
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        // Вывод имени "Мария" через Unicode escape-последовательности
        System.out.println("\u041C\u0430\u0440\u0438\u044F");
    }
}
