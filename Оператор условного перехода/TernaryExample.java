/**
 * Программа для демонстрации замены конструкции if-else на тернарный оператор.
 * 
 * Тернарный оператор ?: является сокращённой формой условного оператора.
 * Формат: (условие) ? (значение если true) : (значение если false)
 * 
 * Данный код заменяет следующую конструкцию:
 * 
 * if (a == 1) {
 *     i = 1;
 * } else if (a == 2) {
 *     i = 2;
 * } else {
 *     i = 3;
 * }
 * 
 * @author Тачилина Мария
 * @version 1.0
 * @since 2026-04-24
 */
public class TernaryExample {
    
    /**
     * Главный метод программы.
     * Демонстрирует работу тернарного оператора с тремя условиями.
     * 
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        
        // Пример с a = 1
        int a = 1;
        int i = (a == 1) ? 1 : (a == 2) ? 2 : 3;
        System.out.println("a = " + a + ", i = " + i);
        
        // Пример с a = 2
        a = 2;
        i = (a == 1) ? 1 : (a == 2) ? 2 : 3;
        System.out.println("a = " + a + ", i = " + i);
        
        // Пример с a = 5 (не 1 и не 2)
        a = 5;
        i = (a == 1) ? 1 : (a == 2) ? 2 : 3;
        System.out.println("a = " + a + ", i = " + i);
    }
}
