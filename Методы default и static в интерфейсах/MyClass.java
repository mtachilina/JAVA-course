/**
 * Программа для демонстрации конфликта default-методов
 * при реализации нескольких интерфейсов.
 * 
 * Если класс реализует два интерфейса с одинаковыми default-методами,
 * возникает конфликт. Класс обязан переопределить конфликтующий метод
 * и может явно вызвать нужную реализацию через Интерфейс.super.метод()
 * 
 * @author Тачилина Мария
 * @version 1.0
 * @since 2026-04-24
 */

/**
 * Первый интерфейс с default-методом print.
 */
interface A {
    
    /**
     * Default-метод интерфейса A.
     */
    default void print() {
        System.out.println("Интерфейс A");
    }
}

/**
 * Второй интерфейс с default-методом print.
 */
interface B {
    
    /**
     * Default-метод интерфейса B.
     */
    default void print() {
        System.out.println("Интерфейс B");
    }
}

/**
 * Класс, реализующий оба интерфейса A и B.
 * Возникает конфликт default-методов print().
 * 
 * Решение: обязательно переопределить метод print() в классе.
 */
public class MyClass implements A, B {
    
    /**
     * Переопределение конфликтующего метода.
     * Обязательно для разрешения конфликта.
     */
    @Override
    public void print() {
        System.out.println("Мой собственный метод");
        
        // Явный вызов метода из интерфейса A
        A.super.print();
        
        // Явный вызов метода из интерфейса B
        B.super.print();
    }
    
    /**
     * Главный метод для демонстрации.
     * 
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        MyClass obj = new MyClass();
        obj.print();
    }
}
