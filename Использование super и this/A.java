/**
 * Программа для демонстрации использования this() для вызова конструкторов.
 * 
 * this() позволяет вызвать один конструктор из другого,
 * избегая дублирования кода. Вызов this() должен быть первой строкой
 * в конструкторе и может использоваться только один раз.
 * 
 * @author Тачилина Мария
 * @version 1.0
 * @since 2026-04-24
 */
class A {
    int a;
    int b;
    int c;
    int z;

    /**
     * Конструктор без параметров.
     * Вызывает конструктор с тремя параметрами, передавая значения по умолчанию.
     */
    public A() {
        this(0, 0, 0);
    }

    /**
     * Конструктор с одним параметром.
     * Вызывает конструктор с тремя параметрами.
     * 
     * @param a значение для поля a
     */
    public A(int a) {
        this(a, 0, 0);
    }

    /**
     * Конструктор с двумя параметрами.
     * Вызывает конструктор с тремя параметрами.
     * 
     * @param a значение для поля a
     * @param b значение для поля b
     */
    public A(int a, int b) {
        this(a, b, 0);
    }

    /**
     * Конструктор с тремя параметрами.
     * Является основным конструктором, выполняющим всю инициализацию.
     * 
     * @param a значение для поля a
     * @param b значение для поля b
     * @param c значение для поля c
     */
    public A(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.z = 1;
    }
}
