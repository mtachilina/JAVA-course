/**
 * Программа для демонстрации переопределения методов (override).
 * 
 * Переопределение - это создание метода в подклассе с таким же именем
 * и параметрами, как в суперклассе.
 * 
 * Важное правило: тип возвращаемого значения при переопределении
 * должен совпадать с типом суперкласса или быть его подтипом.
 * 
 * @author Тачилина Мария
 * @version 1.0
 * @since 2026-04-24
 */

/**
 * Суперкласс Animal.
 */
class Animal {
    
    /**
     * Обычный метод суперкласса.
     */
    public void makeSound() {
        System.out.println("Животное издает звук");
    }
    
    /**
     * Метод, возвращающий объект Animal.
     */
    public Animal getAnimal() {
        return new Animal();
    }
}

/**
 * Подкласс Dog.
 */
class Dog extends Animal {
    
    /**
     * Переопределение метода makeSound.
     * Тип возвращаемого значения void совпадает с родительским.
     */
    @Override
    public void makeSound() {
        System.out.println("Собака лает: Гав-гав");
    }
    
    // Следующий код вызовет ошибку компиляции,
    // так как тип возвращаемого значения String не совпадает
    // с типом Animal из метода суперкласса
    /*
    @Override
    public String getAnimal() {
        return "собака";
    }
    */
    
    // Правильное переопределение метода getAnimal:
    // можно вернуть подтип Animal (например, Dog)
    @Override
    public Dog getAnimal() {
        return new Dog();  // Dog является подтипом Animal
    }
}
