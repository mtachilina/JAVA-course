/**
 * Программа для демонстрации перегрузки методов (overloading).
 * 
 * Перегрузка - это создание нескольких методов с одинаковым именем,
 * но разными параметрами (количество, тип, порядок).
 * 
 * Перегрузка может отличаться:
 * - количеством параметров
 * - типами параметров
 * - порядком параметров
 * 
 * Тип возвращаемого значения на перегрузку НЕ влияет.
 * 
 * @author Тачилина Мария
 * @version 1.0
 * @since 2026-04-24
 */
class Calculator {
    
    /**
     * Сложение двух целых чисел.
     * 
     * @param a первое слагаемое
     * @param b второе слагаемое
     * @return сумма a и b
     */
    public int add(int a, int b) {
        return a + b;
    }
    
    /**
     * Сложение трёх целых чисел.
     * Отличается количеством параметров.
     * 
     * @param a первое слагаемое
     * @param b второе слагаемое
     * @param c третье слагаемое
     * @return сумма a, b и c
     */
    public int add(int a, int b, int c) {
        return a + b + c;
    }
    
    /**
     * Сложение двух чисел с плавающей точкой.
     * Отличается типами параметров.
     * 
     * @param a первое слагаемое
     * @param b второе слагаемое
     * @return сумма a и b
     */
    public double add(double a, double b) {
        return a + b;
    }
    
    /**
     * Конкатенация двух строк.
     * Отличается типами параметров.
     * 
     * @param a первая строка
     * @param b вторая строка
     * @return результат конкатенации
     */
    public String add(String a, String b) {
        return a + b;
    }
}
