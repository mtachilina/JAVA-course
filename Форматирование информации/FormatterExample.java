import java.util.Formatter;

/**
 * Программа для демонстрации использования спецификаторов форматирования.
 * 
 * Спецификаторы формата:
 * - %s - строковое представление аргумента
 * - %d - десятичное целое значение
 * - %f - десятичное значение с плавающей точкой
 * - %b - логическое значение аргумента
 * - %% - вставка знака процента
 * - %n - вставка символа новой строки
 * 
 * @author Тачилина Мария
 * @version 1.0
 * @since 2026-04-24
 */
public class FormatterExample {
    
    /**
     * Главный метод программы.
     * Демонстрирует использование пяти различных спецификаторов форматирования:
     * 
     * 1. %s - для строк
     * 2. %d - для целых чисел
     * 3. %f - для чисел с плавающей точкой
     * 4. %b - для логических значений
     * 5. %% - для вывода символа процента
     * 
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        
        // %s - строковое представление аргумента
        System.out.printf("%s%n", "Привет, мир!");
        
        // %d - десятичное целое значение
        System.out.printf("Число: %d%n", 100);
        
        // %f - десятичное значение с плавающей точкой
        // .2 - указывает на два знака после запятой
        System.out.printf("Число PI: %.2f%n", 3.14159);
        
        // %b - логическое значение аргумента (true/false)
        System.out.printf("Результат: %b%n", true);
        
        // %% - вставка знака процента (экранирование)
        System.out.printf("Скидка 50%%%n");
        
        // %n - вставка символа новой строки (платформонезависимый)
        System.out.printf("Строка 1%nСтрока 2%n");
    }
}
