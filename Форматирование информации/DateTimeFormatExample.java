import java.util.Date;

/**
 * Программа для демонстрации форматирования даты и времени с использованием спецификаторов %t.
 * 
 * Спецификаторы для форматирования времени и даты:
 * - %tH - час в 24-часовом формате (00-23)
 * - %tI - час в 12-часовом формате (01-12)
 * - %tM - минуты (00-59)
 * - %tS - секунды (00-59)
 * - %tL - миллисекунды (000-999)
 * - %tY - год (4 цифры)
 * - %ty - год (2 цифры, 00-99)
 * - %tB - полное название месяца
 * - %tm - месяц (2 цифры, 01-12)
 * - %tA - полное название дня недели
 * 
 * @author Тачилина Мария
 * @version 1.0
 * @since 2026-04-24
 */
public class DateTimeFormatExample {
    
    /**
     * Главный метод программы.
     * Демонстрирует использование пяти спецификаторов для форматирования даты и времени:
     * - %tH (часы)
     * - %tM (минуты)
     * - %tS (секунды)
     * - %tY (год)
     * - %tB (месяц)
     * 
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        Date now = new Date();
        
        // %tH - час в 24-часовом формате (от 00 до 23)
        System.out.printf("Час (00-23): %tH%n", now);
        
        // %tM - минуты (от 00 до 59)
        System.out.printf("Минуты: %tM%n", now);
        
        // %tS - секунды (от 00 до 59)
        System.out.printf("Секунды: %tS%n", now);
        
        // %tY - год в четырёхзначном формате
        System.out.printf("Год: %tY%n", now);
        
        // %tB - полное название месяца (например, "апрель")
        System.out.printf("Месяц: %tB%n", now);
        
        // Дополнительный пример: комбинированный формат даты и времени
        // Для каждого спецификатора нужно передавать один и тот же объект Date
        System.out.printf("Дата и время: %tY-%tm-%td %tH:%tM:%tS%n", now, now, now, now, now, now);
    }
}
